import urlize from '../src/urlize';

function charMapHelper(char_map) {
  for (const char in char_map) {
    const replacement = char_map[char].toLowerCase();
    const expected = (replacement ? `foo-${replacement}-bar-baz` : "foo-bar-baz");
    expect(urlize(`foo ${char} bar baz`)).toEqual(expected);
  }
}

